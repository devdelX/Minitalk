#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*str;
	int		count;
	int		i;

	i = 0;
	count = ft_strlen(src);
	str = (char *)malloc((count + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
